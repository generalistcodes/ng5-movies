/**
 * Movie entity, used for filtering as well.
 */
export class Movie {
  /**
   * @type {number} id Unique numeric identifier.
   */
  id: number;

  /**
   * @type {string} title The title of the movie.
   */
  title: String;

  /**
   * @type {string} overview The overview of the movie.
   */
  overview: String;
}
